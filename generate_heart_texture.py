        [0,1,3,4,3,2,6,6,8,3,4,3,2,0,0,0],
        [0,1,2,3,2,1,2,6,8,3,4,3,2,0,0,0],
        [0,0,1,2,2,1,1,2,6,8,4,3,2,0,0,0],
